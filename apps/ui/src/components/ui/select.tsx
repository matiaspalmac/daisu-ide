export const SelectTrigger = forwardRef<
  ElementRef<typeof SelectPrimitive.Trigger>,
  ComponentPropsWithoutRef<typeof SelectPrimitive.Trigger>
>(({ className, children, ...props }, ref) => (
  <SelectPrimitive.Trigger
    ref={ref}
    className={cn(
      "flex h-8 w-full items-center justify-between gap-2 rounded-[var(--radius-sm)] border border-[var(--border-subtle)] bg-[var(--bg-elevated)] px-3 py-1 text-sm text-[var(--fg-primary)]",
      "hover:border-[var(--border-strong)]",
      "focus-visible:outline-none focus-visible:ring-1 focus-visible:ring-[var(--accent)] focus-visible:shadow-[var(--glow-cyan-sm)]",
      "disabled:cursor-not-allowed disabled:opacity-40",
      "data-[placeholder]:text-[var(--fg-muted)]",
      className,
    )}
    {...props}
  >
    {children}
    <SelectPrimitive.Icon asChild>
      <CaretDown size={12} className="text-[var(--fg-muted)]" />
    </SelectPrimitive.Icon>
  </SelectPrimitive.Trigger>
));
